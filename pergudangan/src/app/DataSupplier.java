     private void loadData (String key){
         try {
             model.setRowCount(0);
             String where = !key.isEmpty() ? "WHERE nama LIKE '%"+key+"%' OR alamat LIKE '%"+key+"%' OR email LIKE '%"+key+"%' OR no_telp LIKE '%"+key+"%'  " : "";
             String q = "SELECT * FROM supplier "+where;
             Connection c = koneksi.MySQL();
             Statement st = c.createStatement();
             ResultSet rs = st.executeQuery(q);
             int nomor = 0;
             while (rs.next()) {
                 int id = rs.getInt("id_supplier");
                 String nama = rs.getString("nama");
                 String alamat = rs.getString("alamat");
                 String email = rs.getString("email");
                 String telp = rs.getString("no_telp");
                         nomor++;
                         Object[] data = {id,nomor, nama, alamat, email, telp};
                         model.addRow(data);    
            }
         } catch (Exception e) {
         }
     }
     
